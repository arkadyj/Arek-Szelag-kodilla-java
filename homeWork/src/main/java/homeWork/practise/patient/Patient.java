package homeWork.practise.patient;

public class Patient {

    private String sName;
    private String fName;

    public Patient(String sName, String fName) {
        this.sName = sName;
        this.fName = fName;
    }

    public String getsName() {
        return sName;
    }

    public String getfName() {
        return fName;
    }
}
